package core.designpatterns.data.ftx;

public class CheckInfo {
	private String checkingAccountName;
	private String checkAccountNumber;
	private String bankRoutingNumber;
	private String checkNumber;
	private String bankName;
	private String bankCity;
	private String bankState;
	private String bankPhone;
	
	public String getCheckingAccountName() {
		return checkingAccountName;
	}
	public void setCheckingAccountName(String checkingAccountName) {
		this.checkingAccountName = checkingAccountName;
	}
	public String getCheckAccountNumber() {
		return checkAccountNumber;
	}
	public void setCheckAccountNumber(String checkAccountNumber) {
		this.checkAccountNumber = checkAccountNumber;
	}
	public String getBankRoutingNumber() {
		return bankRoutingNumber;
	}
	public void setBankRoutingNumber(String bankRoutingNumber) {
		this.bankRoutingNumber = bankRoutingNumber;
	}
	public String getCheckNumber() {
		return checkNumber;
	}
	public void setCheckNumber(String checkNumber) {
		this.checkNumber = checkNumber;
	}
	public String getBankName() {
		return bankName;
	}
	public void setBankName(String bankName) {
		this.bankName = bankName;
	}
	public String getBankCity() {
		return bankCity;
	}
	public void setBankCity(String bankCity) {
		this.bankCity = bankCity;
	}
	public String getBankState() {
		return bankState;
	}
	public void setBankState(String bankState) {
		this.bankState = bankState;
	}
	public String getBankPhone() {
		return bankPhone;
	}
	public void setBankPhone(String bankPhone) {
		this.bankPhone = bankPhone;
	}
	
	
}
